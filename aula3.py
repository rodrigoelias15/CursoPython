"""
    strings e docstring
    strings são imutáveis, ou seja, um caracter não pode ser alterado através do indice gerando erro, como no exemplo: str[i] = 'a'
"""

# Da forma que é mostrado a seguir, não precisamos usar caracter de escape "\" ou backslash

# aspas simples dentro de aspas duplas
print("Rodrigo 'Oliveira'")

# aspas duplas dentro de aspas simples
print('Rodrigo "Oliveira"')

# "r" exibe TODOS caracteres da string, usado para expressões regulares também
print(r"Rodrigo 'Oliveira'")